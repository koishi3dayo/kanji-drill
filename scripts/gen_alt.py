def main():
    # 代替問題は誤変換リスクを避けるため、対象以外の周囲は「かなのまま」にする
    # （復習時のフォールバックであり、正確さ最優先）
    lines = []
    for (k, g, kana, ans, qtype) in ALT_DATA:
        (ka, kb), (aa, ab) = diff_range(kana, ans)
        target = ans[aa:ab]
        reading = kana[ka:kb]
        before = kana[:ka]
        after = kana[kb:]
        tpl = f"{before}{{{{{target}}}}}{after}"
        lines.append('"%s":{tpl:%s,reading:%s,type:%s}' % (
            k, json.dumps(tpl, ensure_ascii=False),
            json.dumps(reading, ensure_ascii=False),
            json.dumps(qtype, ensure_ascii=False)))
    body = "// 復習モード用の代替問題（自動生成）\n// 元の熟語問題が『未習の相方漢字』を含む場合の差し替え先。1漢字1問。\nconst SENTENCE_DATA_ALT = {\n" + ",\n".join(lines) + "\n};\n"
    (BASE / 'sentence-data-alt.js').write_text(body, encoding='utf-8')
    print(f"sentence-data-alt.js を生成: {len(ALT_DATA)}件")

if __name__ == '__main__':
    main()
